void write_spi(unsigned char data)
{
	SPI0.DATA=data;
	while (!(SPI0.INTFLAGS & SPI_RXCIF_bm)){
		;
	}
}

/*********************LCD写数据************************************/
void OLED_DataWrite(unsigned char dat){
	VPORTB.OUT |= PIN4_bm;
	//CS_set_level(true);
	VPORTB.OUT |= PIN1_bm;
	//DC_set_level(true);
	VPORTB.OUT &= ~PIN4_bm;
	//CS_set_level(false);
	write_spi(dat);
	VPORTB.OUT |= PIN4_bm;
	//CS_set_level(true);
	
}
/*********************LCD写命令************************************/
void OLED_CMD(unsigned char cmd)
{
	VPORTB.OUT |= PIN4_bm;
	//CS_set_level(true);
	VPORTB.OUT &= ~PIN1_bm;
	//DC_set_level(false);
	VPORTB.OUT &= ~PIN4_bm;
	//CS_set_level(false);
	write_spi(cmd);
	VPORTB.OUT |= PIN4_bm;
	//CS_set_level(true);
}

/*********************LCD 设置坐标************************************/
void OLED_POS(unsigned char x, unsigned char y)
{
	OLED_CMD(0xb0+y);
	OLED_CMD(((x&0xf0)>>4)|0x10);
	OLED_CMD((x&0x0f)|0x01);
}
/*********************LCD全屏************************************/
void OLED_FILL(unsigned char bmp_dat)
{
	unsigned char y,x;
	for(y=0;y<8;y++)
	{
		OLED_CMD(0xb0+y);
		OLED_CMD(0x01);
		OLED_CMD(0x10);
		for(x=0;x<X_WIDTH;x++)
		OLED_DataWrite(bmp_dat);
	}
}
/*********************LCD复位************************************/
void OLED_CLEAR(void)
{
	unsigned char y,x;
	for(y=0;y<8;y++)
	{
		OLED_CMD(0xb0+y);
		OLED_CMD(0x01);
		OLED_CMD(0x10);
		for(x=0;x<X_WIDTH;x++)
		OLED_DataWrite(0);
	}
}
/*********************LCD初始化************************************/
void OLED_Init(void)
{
	PORTB.OUT &= ~PIN0_bm;
	//RESET_set_level(false);
	_delay_ms(500);
	PORTB.OUT |= PIN0_bm;
	//RESET_set_level(true);
	OLED_CMD(0xae);//--turn off oled panel
	OLED_CMD(0x10);//---set low column address
	OLED_CMD(0x00);//---set high column address
	OLED_CMD(0x40);//--set start line address  Set Mapping RAM Display Start Line (0x00~0x3F)
	OLED_CMD(0x81);//--set contrast control register
	OLED_CMD(0x00); // Set SEG Output Current Brightness
	OLED_CMD(0xa1);//--Set SEG/Column Mapping     0xa0左右反置 0xa1正常
	OLED_CMD(0xc8);//Set COM/Row Scan Direction   0xc0上下反置 0xc8正常
	OLED_CMD(0xa6);//--set normal display
	OLED_CMD(0xa8);//--set multiplex ratio(1 to 64)
	OLED_CMD(0x3f);//--1/64 duty
	OLED_CMD(0xd3);//-set display offset	Shift Mapping RAM Counter (0x00~0x3F)
	OLED_CMD(0x00);//-not offset
	OLED_CMD(0xd5);//--set display clock divide ratio/oscillator frequency
	OLED_CMD(0x80);//--set divide ratio, Set Clock as 100 Frames/Sec
	OLED_CMD(0xd9);//--set pre-charge period
	OLED_CMD(0xf1);//Set Pre-Charge as 15 Clocks & Discharge as 1 Clock
	OLED_CMD(0xda);//--set com pins hardware configuration
	OLED_CMD(0x12);
	OLED_CMD(0xdb);//--set vcomh
	OLED_CMD(0x40);//Set VCOM Deselect Level
	OLED_CMD(0x20);//-Set Page Addressing Mode (0x00/0x01/0x02)
	OLED_CMD(0x02);//
	OLED_CMD(0x8d);//--set Charge Pump enable/disable
	OLED_CMD(0x14);//--set(0x10) disable
	OLED_CMD(0xa4);// Disable Entire Display On (0xa4/0xa5)
	OLED_CMD(0xa6);// Disable Inverse Display On (0xa6/a7)
	OLED_CMD(0xaf);//--turn on oled panel
	OLED_FILL(0x00);  //初始清屏
	OLED_POS(0,0);
}


/***************功能描述：显示6*8一组标准ASCII字符串	显示的坐标（x,y），y为页范围0～7****************/
void OLED_6X8_String(uchar x,uchar y,uchar ch[])
{
	unsigned char c=0,i=0,j=0;
	while (ch[j]!='\0')
	{
		c =ch[j]-32;
		if(x>126){x=0;y++;}
		OLED_POS(x,y);
		for(i=0;i<6;i++)
		OLED_DataWrite(FASIIC[c][i]);
		x+=6;
		j++;
	}
}

void LCD_shuzi(uchar x,uchar y,uchar ch)
{
	unsigned char c=0,i=0;

	c =ch+16;
	OLED_POS(x,y);
	for(i=0;i<6;i++)
	OLED_DataWrite(FASIIC[c][i]);
}

